// Package storage определяет общие типы и ошибки, используемые различными
// реализациями хранилищ данных в приложении (например, PostgreSQL, Redis).
// Это позволяет создать единый контракт для взаимодействия с хранилищами
// и избежать дублирования кода.
package storage

import "errors"

// Пакетные переменные, определяющие стандартные доменные ошибки.
// Использование предопределенных ошибок позволяет вызывающему коду
// надежно проверять тип ошибки (через `errors.Is`) и принимать
// соответствующие решения, не завязываясь на конкретный текст ошибки.
var (
	// ErrNoOrder сигнализирует о том, что запрашиваемый заказ
	// не был найден в хранилище.
	ErrNoOrder = errors.New("no order found")

	// ErrEmptyOrder может использоваться, если заказ найден, но
	// он не содержит товаров, что может считаться невалидным состоянием.
	ErrEmptyOrder = errors.New("no items in order")
)
